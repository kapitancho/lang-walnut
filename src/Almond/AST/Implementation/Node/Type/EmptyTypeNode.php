<?php

namespace Walnut\Lang\Almond\AST\Implementation\Node\Type;

use Walnut\Lang\Almond\AST\Blueprint\Node\SourceLocation;
use Walnut\Lang\Almond\AST\Blueprint\Node\Type\EmptyTypeNode as EmptyTypeNodeInterface;

final readonly class EmptyTypeNode implements EmptyTypeNodeInterface {
	public function __construct(
		public SourceLocation $sourceLocation
	) {}

	public function children(): iterable {
		return [];
	}

	public function jsonSerialize(): array {
		return [
			'sourceLocation' => $this->sourceLocation,
			'nodeCategory' => 'Type',
			'nodeName' => 'EmptyType',
		];
	}
}